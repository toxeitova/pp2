#casting - менять типы данных
#меняем на int
x = int(8)
y = int(2.2)
z = int("7")
print(x)
print(y)
print(z)

#меняем на float
x = float(9)
y = float(9.2)
z = float("5")
w = float("99.9")
print(x)
print(y)
print(z)
print(w)

#меняем на strings
x = str("lala")
y = str(8)
z = str(0)
print(x)
print(y)
print(z)
print(x+y)

